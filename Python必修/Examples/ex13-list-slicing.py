for i in range(10):
    alist.append(i)
    
print(f"alist[0]: {alist[0]}")
print(f"alist[3]: {alist[3]}")
print(f"alist[-5]: {alist[-5]}")
print(f"alist[5:]: {alist[5:]}")
print(f"alist[-5:]: {alist[-5:]}")
print(f"alist[:5]: {alist[:5]}")
print(f"alist[5::2]: {alist[5::2]}")
print(f"alist[::-1]: {alist[::-1]}")
print(f"alist[-5:-11:-1]: {alist[-5:-11:-1]}")

